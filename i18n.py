def translate(text, lang):
    if lang == "zh":
        return TRANSLATIONS["zh"].get(text, text)
    return text


def current_locale():
    """从请求/会话推断当前语言（供后端 flash 等使用）。"""
    try:
        from flask import session, request, current_app
        lang = session.get("lang") or request.cookies.get("lang")
        if lang in SUPPORTED:
            return lang
        best = request.accept_languages.best_match(SUPPORTED)
        return best or current_app.config.get("BABEL_DEFAULT_LOCALE", DEFAULT)
    except Exception:
        return DEFAULT


def _(text):
    """后端翻译函数：在请求上下文中按当前语言翻译。"""
    return translate(text, current_locale())
